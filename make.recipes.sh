    for stat in ${ADMIN_PODS_PRUNE}; do 
        echo -e "\nüîß Delete all Pods having STATUS: $stat"
        kubectl get pod -A -o wide |grep '\b'$stat'\b' |awk '{print $1,$2}' |xargs -n2 /bin/bash -c '
            [[ $2 ]] || {
